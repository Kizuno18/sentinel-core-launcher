/**
 * Sidebar — Icon-based vertical navigation with social links.
 * Navigation icons are fixed; social links come from server config (white-label).
 */
function Sidebar({ currentPage, onNavigate }: SidebarProps) {
  const config = useConfig();
  const socialEntries = Object.entries(config.server.socials).filter(([, url]) => url);

  return (
    <aside className="sidebar">
      {/* Navigation items */}
      <nav className="sidebar-nav">
        {NAV_ITEMS.map((item) => (
          <button
            key={item.id}
            className={`sidebar-nav-item ${currentPage === item.id ? "active" : ""}`}
            onClick={() => onNavigate(item.id)}
            title={item.label}
          >
            <NavIcon icon={item.icon} active={currentPage === item.id} />
            {currentPage === item.id && <div className="sidebar-active-indicator" />}
          </button>
        ))}
      </nav>

      {/* Social links — from server config */}
      {socialEntries.length > 0 && (
        <div className="sidebar-socials">
          {socialEntries.map(([type, url]) => (
            <a
              key={type}
              href={url}
              className="sidebar-social-link"
              title={type.charAt(0).toUpperCase() + type.slice(1)}
              target="_blank"
              rel="noopener noreferrer"
            >
              <SocialIcon type={type} />
            </a>
          ))}
        </div>
      )}
    </aside>
  );
}

export default Sidebar;
